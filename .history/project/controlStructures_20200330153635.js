const id = '100';

//EQUAL TO
if (id == 100) {
    console.log('correct');
}else{
    console.log('Incorrect');
}

//NOT EQUAL TO
if (id != 100) {
    console.log('correct');
}else{
    console.log('Incorrect');
}


//EQUAL TO VALUE AD TYPE
if (id === 100) {
    console.log('correct');
}else{
    console.log('Incorrect');
}

//EQUAL TO VALUE AD TYPE
if (id !== 100) {
    console.log('correct');
}else{
    console.log('Incorrect');
}

